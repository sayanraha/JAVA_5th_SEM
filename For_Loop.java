public class For_Loop {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }
        int n = 5;
        for (int i = 0; i < n; i++) {
            System.out.println(i);
        }
        for (int i = 5; i != 0; i--){
            System.out.println(i);
        }
    }
}
