public class Type_char {
    public static void main(String args[]){
        char letter = 'A';
        System.out.println(letter);

        char number_as_text = '6';
        System.out.println(number_as_text);

        char number = 65;
        System.out.println(number); //ASCII VALUE WILL SHOW i.e; 65 = A
    }
}
